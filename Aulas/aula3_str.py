"""
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(123)

# aspas simples
print('Pedro Ferreira')

# Aspas duplas
print("Pedro Ferreira")

# Escape
print("Pedro \"Ferreira\"") # serve para não identificar o comando ""

# r 
print(r"Pedro \"Ferreira\"") # maneira fácil = aspa simples e dupla

# exemplo de um modo mais simples
print('Pedro "Ferreira"')

